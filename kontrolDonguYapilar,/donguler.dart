void main(List<String> args) {
  //donguler loop
  // for dongusu

  for (int i = 0; i < 10; i++) {
    print(" ${i + 1} hasan ");
  }

  //while
  int sayac = 0;

  while (sayac < 10) {
    print(" ${sayac + 1} while ");
    sayac++;
  }

  //do - while
  int sayac1 = 0;
  do {
    print("do while ${sayac1 + 1}");
    sayac1++;

  } while (sayac1 < 5);
}

